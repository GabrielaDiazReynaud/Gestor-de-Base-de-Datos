    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        index = 0;
        tablename = "";
        JList list = (JList) evt.getSource();
        if (evt.getClickCount() == 2) {
            index = list.locationToIndex(evt.getPoint());
            tablename = jList1.getModel().getElementAt(index).toString();
            System.out.print(tablename);
            DefaultTableModel n2 = Columns(tablename);
            ArrayList<String> temp = GestorBaseDatos.conexion.TablesData(tablename);
            int ctn = 0;
            while (ctn < temp.size()) {
                Object[] obj = new Object[GestorBaseDatos.conexion.columnas];
                for (int x = 0; x < GestorBaseDatos.conexion.columnas; x++) {
                    obj[x] = temp.get(ctn);
                    ctn++;
                }
                n2.addRow(obj);
            }
        }


    }//GEN-LAST:event_jList1MouseClicked

