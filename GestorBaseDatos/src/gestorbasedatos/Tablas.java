    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        DefaultTableModel n2 = (DefaultTableModel) jTable1.getModel();
        int x = 0;
        String val = "";
        if (n2.getRowCount() != 0) {
            x = n2.getRowCount() - 1;
             ArrayList<String> tipos= GestorBaseDatos.conexion.types(tablename);
            for (int y = 0; y < n2.getColumnCount(); y++) {
              if(tipos.get(y).equals("VARCHAR") ||tipos.get(y).equals("DATE")){
                val += " '" + n2.getValueAt(x, y).toString()+"'";
              }
              else{
                 val += " " + n2.getValueAt(x, y).toString()+" ";
              }
                if (y != n2.getColumnCount()-1) {
                    val += " ,";
                }
            }
              
            GestorBaseDatos.conexion.insertInfoTable(val,tablename);
        }
       
    }//GEN-LAST:event_jButton7MouseClicked

