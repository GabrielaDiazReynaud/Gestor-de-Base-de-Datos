    private void jButton8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton8MouseClicked
      ArrayList<String> tipos= GestorBaseDatos.conexion.types(tablename);
        String PK= GestorBaseDatos.conexion.getprimarykey(tablename)+"= ";
        DefaultTableModel n= new DefaultTableModel();
        int indexC=jTable1.getSelectedColumn();
        int indexR=jTable1.getSelectedRow();
        String name= jTable1.getColumnName(indexC);
        String value=" "+name+"= ";
        int pkindex=0;
        if(tipos.get(indexC).equals("VARCHAR") ||tipos.get(indexC).equals("DATE")){
          value+="'"+ jTable1.getValueAt(indexR, indexC).toString()+" '";
             
        }
        else{
          value+=jTable1.getValueAt(indexR, indexC).toString();  
        }
        
        int count= jTable1.getColumnCount();
        for(int x=0;x<count; x++){
            String tname= jTable1.getColumnName(x);
            if(tname.equals(PK)){
                pkindex=x;
                
            }           
        }
        if(tipos.get(pkindex).equals("VARCHAR") ||tipos.get(pkindex).equals("DATE")){
          PK+="'"+ jTable1.getValueAt(indexR,pkindex).toString()+" '";
             
        }
        else{
          PK +=jTable1.getValueAt(indexR,pkindex).toString(); 
        }
        GestorBaseDatos.conexion.modificarInfoTable(tablename, PK, value);
       
    }//GEN-LAST:event_jButton8MouseClicked

